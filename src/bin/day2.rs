fn main() {
    // shared code, iterator with all parsed commands
    let iter = INPUT
        .lines()
        .filter(|x| !x.is_empty())
        .map(|line| {
            let mut it = line.split_whitespace();
            let res = (
                it.next().expect("cmd to be there"),
                it.next().expect("value to be there"),
            );
            assert_eq!(it.next(), None, "No leftovers, we parsed everything");
            res
        })
        .map(|(cmd, val)| (cmd, val.parse::<i32>().unwrap()));

    // Part A:
    let (pos, depth) = iter
        .clone()
        .fold((0, 0), |(pos, depth), (cmd, val)| match cmd {
            "forward" => {
                let pos = pos + val;
                (pos, depth)
            }
            "down" => {
                let depth = depth + val;
                (pos, depth)
            }
            "up" => {
                let depth = depth - val;
                (pos, depth)
            }
            _ => {
                unreachable!("Unrecognised command")
            }
        });

    let total = depth * pos;
    println!("Solution to part A: {}", total);

    // Part B

    let (aim, pos, depth) = iter.fold((0, 0, 0), |(aim, pos, depth), (cmd, val)| match cmd {
        "forward" => {
            let pos = pos + val;
            let depth = depth + (aim * val);
            (aim, pos, depth)
        }
        "down" => {
            let aim = aim + val;
            (aim, pos, depth)
        }
        "up" => {
            let aim = aim - val;
            (aim, pos, depth)
        }
        _ => {
            unreachable!("Unrecognised command")
        }
    });

    let final_answer = pos * depth;

    println!(
        "Part B: aim: {}, horizontal position: {}, depth: {}. Final answer is: {}",
        aim, pos, depth, final_answer
    );
}

